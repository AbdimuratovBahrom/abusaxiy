// Обработка изменения блока
blockSelect.addEventListener("change", function() {
  const block = this.value;

  result.innerHTML = "";
  rowSelect.innerHTML = `<option value="">Выберите ряд</option>`;
  storeSelect.innerHTML = `<option value="">Выберите магазин</option>`;
  rowSelect.disabled = true;
  storeSelect.disabled = true;

  if (!block || !DATA[block]) return;

  // Специальные блоки без рядов
  if (block === "Гипермаркет" || block === "Специфические объекты") {
    rowSelect.innerHTML = '<option value="">— без рядов —</option>';
    rowSelect.disabled = true;

    const allShops = new Set();
    (DATA[block].paths || []).forEach(item => {
      item.shops.forEach(shop => {
        if (shop && shop.trim()) allShops.add(shop.trim());
      });
    });

    const sortedShops = [...allShops].sort((a, b) => {
      const na = parseFloat(a) || 999999999;
      const nb = parseFloat(b) || 999999999;
      return na !== nb ? na - nb : a.localeCompare(b, 'ru');
    });

    storeSelect.innerHTML = '<option value="">Выберите магазин</option>';
    sortedShops.forEach(shop => {
      const opt = document.createElement('option');
      opt.value = shop;
      opt.textContent = shop;
      storeSelect.appendChild(opt);
    });

    storeSelect.disabled = sortedShops.length === 0;
    return;
  }

  // Обычные блоки с рядами
  const rows = new Set();
  (DATA[block].paths || []).forEach(item => {
    const match = item.path.match(/Ряд\s*([^ >]+)/i);
    if (match) rows.add(match[1].trim());
  });

  [...rows].sort((a, b) => {
    const na = parseInt(a) || 999;
    const nb = parseInt(b) || 999;
    return na !== nb ? na - nb : a.localeCompare(b);
  }).forEach(r => {
    const opt = document.createElement("option");
    opt.value = r;
    opt.textContent = `Ряд ${r}`;
    rowSelect.appendChild(opt);
  });

  rowSelect.disabled = rows.size === 0;
});

