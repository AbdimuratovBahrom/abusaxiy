// Буква "g" в номере магазина — питание от ТП-5353 (см. GRAPH_REPORT / данные ниже)
function feedNoteForShops(shops) {
  const hasG = (shops || []).some(s => {
    const v = String(s).trim();
    return /g$/i.test(v) && /\d/.test(v); // например 19g, 12ag, 12bg
  });
  return hasG
    ? `<div style="font-size:0.78rem;color:#0369a1;margin-top:2px">⚡ Буква «g» в номере магазина — питание от ТП-5353</div>`
    : '';
}

function renderPath(pathStr) {
  const parts = pathStr.split(' > ');
  const chips = parts.map((p, i) => {
    const type = getChipType(p);
    const title = CHIP_TITLES[type] ? ` title="${escapeHTML(CHIP_TITLES[type])}"` : '';
    const sep = i < parts.length - 1 ? '<span class="path-sep">›</span>' : '';
    return `<span class="path-chip chip-${type}"${title}>${escapeHTML(p)}</span>${sep}`;
  }).join('');

  const escaped = escapeHTML(pathStr);
  return `<div class="ai-path">
    <div class="path-chips">${chips}</div>
    <button class="copy-path-btn" onclick="copyPath(this)" data-path="${escaped}" title="Скопировать путь">📋</button>
  </div>`;
}

function formatAIMessage(text) {
  if (!text || typeof text !== 'string') return '<div class="ai-text">ИИ не смог ответить.</div>';
  let html = '';
  const lines = text.split('\n');
  let inList = false;

  for (const rawLine of lines) {
    const line = rawLine.trim();

    if (!line) {
      if (inList) { html += '</ul>'; inList = false; }
      html += '<div class="ai-spacer"></div>';
      continue;
    }

    // Строка с путём (содержит ' > ' и начинается с известного элемента)
    if (line.includes(' > ') && /^(ТП-|АВР|ВРУ|ЯРВ|ЩР|[Шш]ит|1-блок|2-блок|3-блок|Гипермаркет)/i.test(line)) {
      if (inList) { html += '</ul>'; inList = false; }
      html += renderPath(line);
      continue;
    }

    // Форматирование текста
    let formatted = line
      .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>')
      .replace(/`([^`]+)`/g, '<code class="inline-code">$1</code>');

    if (line.startsWith('- ') || line.startsWith('• ')) {
      if (!inList) { html += '<ul class="ai-list">'; inList = true; }
      html += `<li>${formatted.replace(/^[-•]\s*/, '')}</li>`;
    } else if (/^\d+\.\s/.test(line)) {
      if (inList) { html += '</ul>'; inList = false; }
      html += `<div class="ai-numbered">${formatted}</div>`;
    } else {
      if (inList) { html += '</ul>'; inList = false; }
      html += `<div class="ai-text">${formatted}</div>`;
    }
  }

  if (inList) html += '</ul>';
  return html;
}

